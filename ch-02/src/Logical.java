public class Logical {
    public static void main(String[] args) {
        System.out.println(3>2);
        System.out.println(3<2);
        System.out.println(3==2);
        System.out.println(3!=2);
        System.out.println(!(3!=2));
        System.out.println((3>2||3>4));
        System.out.println(3>2&&3>4);
    }
}
